#include <stdio.h>
int main()
{
  int k=0;
  int i,j,i_,j_;

  scanf("%d",&k);
  if(k>4){
    j_=(k-4)%3-1;
    if(j_<0){
      j_=j_+4;
    }
    if(j_>-1&&j_<3){
      i_=2*((k-4)/3+2)-1;
    }
    else{
      i_=2*((k-4)/3+1)-1;
    }
  }//行列截止数额
  if(k<=4){
    i_=1;
    j_=k-1;
  }//行列截止数额
  printf("+------------------------+\n");
  for(j=0;j<4;j++){
    for(i=0;i<27;i++){
    if(i==0){
      printf("|");
    }//if_第一列判断
    if(k<=4&&j<=j_&&i==1){
      printf("O");
    }//if_第一列座位4座判断
    if(k<=4&&j>j_&&i==1&&j==2){
      printf("#");
    }//if_第一列座位4座判断
    if(k>4&&i==1){
      printf("O");
    }//if_第一列座位4座判断
    if(i<24&&i%2==0&&i>=2&&(j==0||j==1||j==3)){
      printf(".");
    }//if_非过道行的.判断
    if(i<22&&i%2==1&&i>=2&&i<i_&&j!=2){
      printf("O");
    }//if_在非过道行的座位有人
    if(i<22&&i>i_&&i%2==0&&j!=2){
      printf("#");
    }//if_非过道行的座位无人
    if(i%2==1&&i>=2&&i==i_&&j<=j_&&j!=2){
      printf("O");
    }//if_在非过道行的座位有人
    if(i<22&&i==i_&&i%2==1&&j>j_&&j!=2){
        printf("#");
    }//if_非过道行的座位无人
    if(i==24&&j!=0){
      printf(".");
    }//if_驾驶位置的.
    else if(i==24&&j==0){
      printf("D");
    }//if_驾驶员
    if(j==2&&i>2&&i<25){
      printf(".");
    }//if_过道.
    if(i==23&&j!=2||i==25){
      printf("|");
    }//if车挡板
    if(i==26&&(j==0||j==3)){
      printf(")\n");
    }//if前车车窗
    if(i==26&&(j==1||j==2)){
      printf("\n");
	  break;
    }
   }//for_i
  }//for_j
  printf("+------------------------+");
}
